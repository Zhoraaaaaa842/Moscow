// Получить все уникальные округа
export function getOkrugList() {
  const set = new Set(Object.values(DISTRICTS_META).map(d => d.okrug));
  return ['Все', ...Array.from(set).sort()];
}

// Получить районы по округу
export function getDistrictsByOkrug(okrug) {
  if (okrug === 'Все') return Object.keys(DISTRICTS_META);
  return Object.keys(DISTRICTS_META).filter(k => DISTRICTS_META[k].okrug === okrug);
}

